CREATE TABLE IF NOT EXISTS `merchant` (
  `id` bigint(20) NOT NULL,
  `address` varchar(500) DEFAULT NULL,
  `bankaccount` varchar(50) DEFAULT NULL,
  `bankaccountname` varchar(50) DEFAULT NULL,
  `bankname` varchar(50) DEFAULT NULL,
  `close_hour` varchar(255) DEFAULT NULL,
  `created_by` varchar(100) DEFAULT NULL,
  `created_date` datetime DEFAULT NULL,
  `email` varchar(100) DEFAULT NULL,
  `is_deleted` bit(1) DEFAULT NULL,
  `last_login` datetime DEFAULT NULL,
  `open_hour` varchar(255) DEFAULT NULL,
  `operation_number` varchar(20) DEFAULT NULL,
  `password` varchar(255) DEFAULT NULL,
  `phone` varchar(18) DEFAULT NULL,
  `picture` varchar(255) DEFAULT NULL,
  `pin` int(11) DEFAULT NULL,
  `status` int(11) DEFAULT NULL,
  `store_name` varchar(100) DEFAULT NULL,
  `updated_by` varchar(100) DEFAULT NULL,
  `updated_date` datetime DEFAULT NULL,
  `username` varchar(100) DEFAULT NULL,
  `working_day` varchar(255) DEFAULT NULL,
  `about` text DEFAULT NULL,
  `token_update` text DEFAULT NULL,
  `preset_message` text DEFAULT NULL,
  `city` varchar(255) DEFAULT NULL,
  `postal_code` varchar(255) DEFAULT NULL,
  `available_delivery` varchar(20) DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;